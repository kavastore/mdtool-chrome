/**
 * Type-safe wrapper around chrome.i18n.getMessage.
 * Falls back to the key itself so UI never shows blank strings.
 */
export function t(key: MessageKey, substitutions?: string | string[]): string {
  const msg = chrome.i18n.getMessage(key, substitutions)
  return msg || key
}
